    BLOOD_GROUP VARCHAR2(6),
    address VARCHAR2(100),
    city VARCHAR2(20),
    pincode NUMBER(6,0),
    CREATED_DATE DATE DEFAULT SYSDATE,
    UPDATED_DATE DATE DEFAULT SYSDATE,
    CONSTRAINT DOCTOR_DETAILS_id_PK PRIMARY KEY(id)
);

